class Result {

    /*
     * Complete the 'pickingNumbers' function below.
     *
     * The function is expected to return an INTEGER.
     * The function accepts INTEGER_ARRAY a as parameter.
     */

    public static int pickingNumbers(List<Integer> a) {
    HashMap<Integer, Integer> numbers = new HashMap<>();
    
    for(int values : a){
        if(numbers.containsKey(values)){
            numbers.put(values, numbers.get(values)+1);
        }else{
            numbers.put(values, 1);
        }
    }
    int maxValue = 0;
    for (int key : numbers.keySet()) {
        int current = numbers.get(key);
        int next = numbers.getOrDefault(key + 1, 0);

        maxValue = Math.max(maxValue, current + next);
    }

    return maxValue;
}
    
}

public class Solution {
    public static void main(String[] args) throws IOException {
        BufferedReader bufferedReader = new BufferedReader(new InputStreamReader(System.in));
        BufferedWriter bufferedWriter = new BufferedWriter(new FileWriter(System.getenv("OUTPUT_PATH")));

        int n = Integer.parseInt(bufferedReader.readLine().trim());

        List<Integer> a = Stream.of(bufferedReader.readLine().replaceAll("\\s+$", "").split(" "))
            .map(Integer::parseInt)
            .collect(toList());

        int result = Result.pickingNumbers(a);

        bufferedWriter.write(String.valueOf(result));
        bufferedWriter.newLine();

        bufferedReader.close();
        bufferedWriter.close();
    }
}
